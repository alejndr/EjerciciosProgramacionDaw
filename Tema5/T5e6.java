/**
 * Muestra los números del 320 al 160, contando de 20 en 20 hacia atrás
 * utilizando un bucle do while .
 *
 * @author Alejandro MV
 */
public class T5e6 {
  public static void main(String[] args) {
    
    int i = 340;

    do {
    i = i-20;
    System.out.println(i);
      
    } while (i >= 180);
  }
}
